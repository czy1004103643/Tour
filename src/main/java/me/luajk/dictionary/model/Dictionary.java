package me.luajk.dictionary.model;

public class Dictionary {

    private int id;
    private String value;

    public Dictionary() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
